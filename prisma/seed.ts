// Главная функция — запускается при выполнении скрипта
async function main() {
	console.log('🌱 Заполняем базу данных...')

	// Проходим по каждой категории
	for (const [categoryName, data] of Object.entries(categoriesData)) {
		// upsert = "вставь или обнови"
		// Если категория уже есть — не создаёт дубликат
		const category = await prisma.category.upsert({
			where: { slug: data.slug },
			update: {}, // Если уже есть — ничего не меняем
			create: { name: categoryName, slug: data.slug }, // Если нет — создаём
		})

		// Для каждого слова в категории
		for (const wordText of data.words) {
			// Создаём слово (или находим существующее)
			const word = await prisma.word.upsert({
				where: { text: wordText },
				update: {},
				create: { text: wordText },
			})

			// Создаём связь слово-категория
			await prisma.wordCategory.upsert({
				where: {
					wordId_categoryId: { wordId: word.id, categoryId: category.id },
				},
				update: {},
				create: { wordId: word.id, categoryId: category.id },
			})
		}

		console.log(`  ✅ ${categoryName}: ${data.words.length} слов`)
	}

	console.log('🎉 База данных заполнена!')
}

// Запускаем функцию
main()
	.catch(e => {
		console.error(e)
		process.exit(1)
	})
	.finally(async () => {
		await prisma.$disconnect() // Закрываем соединение с БД
	})
